import { chromium } from "playwright";
import { fileURLToPath } from "url";
import { dirname, join } from "path";
import { writeFileSync } from "fs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PUBLIC = join(__dirname, "remotion", "public");
const SRC = join(__dirname, "remotion", "src");
const URL = "http://localhost:3000/editor";
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

const run = async () => {
  const browser = await chromium.launch({ headless: true });
  const page = await browser.newPage({ viewport: { width: 1920, height: 1080 }, deviceScaleFactor: 2 });

  await page.goto(URL, { waitUntil: "networkidle" });
  await page.waitForSelector(".react-flow__viewport", { timeout: 30000 });
  await page.click(".react-flow__controls-fitview").catch(() => {});
  await sleep(1200);

  // Pane rect + each node's rect, as fractions of the pane (== fractions of the screenshot).
  const data = await page.evaluate(() => {
    const pane = document.querySelector(".react-flow");
    const pr = pane.getBoundingClientRect();
    const nodes = [...document.querySelectorAll(".react-flow__node")].map((n) => {
      const r = n.getBoundingClientRect();
      const cls = n.className || "";
      const m = cls.match(/react-flow__node-(\w+)/);
      return {
        type: m ? m[1] : "node",
        x: (r.left - pr.left) / pr.width,
        y: (r.top - pr.top) / pr.height,
        w: r.width / pr.width,
        h: r.height / pr.height,
      };
    });
    return { paneW: pr.width, paneH: pr.height, nodes };
  });

  const pane = await page.$(".react-flow");
  await pane.screenshot({ path: join(PUBLIC, "editor-clean.png") });

  const ts =
    `// AUTO-GENERATED by demo-reel/capture-editor-glow.mjs — node rects as fractions of editor-clean.png\n` +
    `export const EDITOR_ASPECT = ${(data.paneW / data.paneH).toFixed(5)};\n` +
    `export const EDITOR_NODES = ${JSON.stringify(data.nodes, null, 2)} as const;\n`;
  writeFileSync(join(SRC, "editorNodes.ts"), ts);

  console.log(`saved editor-clean.png + editorNodes.ts (${data.nodes.length} nodes, aspect ${(data.paneW / data.paneH).toFixed(3)})`);
  data.nodes.forEach((n) => console.log(`  ${n.type}: x=${n.x.toFixed(3)} y=${n.y.toFixed(3)} w=${n.w.toFixed(3)} h=${n.h.toFixed(3)}`));

  await browser.close();
};

run().catch((e) => { console.error(e); process.exit(1); });
